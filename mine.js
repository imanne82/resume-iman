function card() {
    let data = document.getElementById("datalist");
    data.style.height = "100%";
    data.style.opacity = "1";
    data.style.color = "white";
    data.style.fontSize = "20px";
}

function buttonBack() {
    let data = document.getElementById("datalist");
    data.style.opacity = "0";
    data.style.height = "0";

}